function HtmlEnc([string]$s) { return $s -replace '&', '&amp;' -replace '<', '&lt;' -replace '>', '&gt;' -replace '"', '&quot;' }
function JsonEnc([string]$s) { return $s -replace '\\', '\\\\' -replace '"', '\"' -replace "`r", '' -replace "`n", '\n' }

